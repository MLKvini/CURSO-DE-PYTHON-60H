soma = num1 + num2
print('ESSE É O RESULTADO DA SOMA:',soma)

menos = num1 - num2
print('ESSE É O RESULTADO DA SUBTRAÇÃO:',menos)

multiplicacao = num1 * num2
print('ESSE É O RESULTADO DA MULTIPLICACÃO:',multiplicacao)

divisao = num1 / num2
print('ESSE É O RESULTADO DIVISÃO:',divisao)
